const frases = document.querySelectorAll('.frase')

function identifyActive(){
  let positionFrase;
  frases.forEach((frase, index) =>{
    if(frase.classList.contains("active")){
      positionFrase = index
    }
  })

  return positionFrase
}

let positionFrase = identifyActive()

function modifyFrase(positionFrase){
  frases[positionFrase].classList.remove("active")
  if(positionFrase + 1 > 2){
    frases[0].classList.add("active")
  }else{
    frases[positionFrase + 1].classList.add("active")
  }
}

setInterval(() =>{
  modifyFrase(positionFrase)
  identifyActive()
  positionFrase = identifyActive()
}, 5000)



